    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
        // TODO add your handling code here:
        
        boolean error=false;
        
        do
        {
            if(tfCodigo.getText()!=null||tfNombre.getText()!=null||taDescripcion.getText()!=null||tfDni.getText()!=null )
            {
                Aviso av=Programa.buscarAviso(tfCodigo.getText());
                
                
                if(av!=null)
                {
                    
                    Programa.actualizarAviso(tfCodigo.getText(),tfNombre.getText(),taDescripcion.getText(),tfDni.getText());
                    tfCodigo.setText(av.getCodAviso());
                    tfNombre.setText(av.getNombre());
                    taDescripcion.setText(av.getDescripcion());
                    tfDni.setText(av.getT().getDni());
                    
                }
                else
                {
                    Programa.insertarAviso(tfCodigo.getText(),tfNombre.getText(),taDescripcion.getText(),tfDni.getText());
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"campo vacio");
                error=true;
            }
        }
        while(error==true);
    }//GEN-LAST:event_btAceptarActionPerformed


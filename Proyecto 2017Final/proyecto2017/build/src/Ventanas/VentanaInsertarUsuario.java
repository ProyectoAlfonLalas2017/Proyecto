    private void jBtAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtAceptarActionPerformed

        boolean error=false;
        
        do
        {
            if(jTfUsuario.getText()!=null||new String(jPassfContraseña.getPassword())!=null||tfDni.getText()!=null)
            {
                
                if(Programa.buscarTrabajadorLog(jTfUsuario.getText(),new String(jPassfContraseña.getPassword())) == null )
                {

                    Programa.insertarUsuraio(jTfUsuario.getText(),new String(jPassfContraseña.getPassword()),tfDni.getText());
                }
                else
                {


                    Programa.actualizarUsuario(jTfUsuario.getText(),new String(jPassfContraseña.getPassword()),tfDni.getText());
                    
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"campo vacio");
                error=true;
            }
        }
        while(error==true);
    }//GEN-LAST:event_jBtAceptarActionPerformed

